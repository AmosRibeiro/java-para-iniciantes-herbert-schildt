public class Escada {
    public static void main(String[] args) {
        int x;

        for(x = 0; x < 6; x++){
            if(x==1)
            System.out.println("x é um.");
            else if (x==2)
            System.out.println("x é dois.");
            else if (x==3)
            System.out.println("x é três.");
            else if (x==4)
            System.out.println("x é quatro.");
            else
            System.out.println("x não está entre 1 e 4.");
        }
    }
}
