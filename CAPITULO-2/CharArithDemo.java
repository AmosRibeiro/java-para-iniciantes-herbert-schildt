// Variáveis de caracteres podem ser tratadas como inteiros.
public class CharArithDemo {
    public static void main(String[] args) {
        char ch;

        ch = 'X';
        System.out.println("A variável ch contém: " + ch);

        ch++; //incrementa ch
        System.out.println("ch agora é: " + ch);

        ch=90; //dá a ch o valor de z
        System.out.println("ch é: " + ch);
    }
}
